// Command gen vendors selected MySQL scrapers from the pinned
// github.com/prometheus/mysqld_exporter git submodule (pkg/vendor/...) into the
// sibling `scrapers` package.
//
// It copies each source file verbatim — preserving the upstream Apache-2.0
// copyright header for attribution — rewriting only the package clause, and
// rewrites instance.go so the unexported `instance` type is constructed from a
// caller-provided *sql.DB instead of opening its own pool from a DSN. This lets
// the instance manager reuse its existing connection management while keeping
// the upstream scrapers byte-for-byte otherwise, so future submodule bumps are a
// re-run of `go generate` plus a build.
package main

import (
	"flag"
	"fmt"
	"log"
	"os"
	"path/filepath"
	"regexp"
	"strings"
)

// genMarker tags every emitted file so a re-run can purge stale output without
// touching the hand-written files (runner.go, doc.go) in the same package.
const genMarker = "// Code generated by metrics/scrapers/gen; DO NOT EDIT."

const genHeader = genMarker + "\n" +
	"// Source: github.com/prometheus/mysqld_exporter (Apache-2.0), vendored at\n" +
	"// pkg/vendor/prometheus/mysqld_exporter. Re-run `go generate ./...` to update.\n\n"

// supportFiles carry the shared interface, helpers, subsystem consts and the
// adapted instance type every scraper depends on.
var supportFiles = []string{
	"scraper.go",
	"collector.go",
	"instance.go",
	"info_schema.go",
	"perf_schema.go",
}

// scraperFiles are the individual MySQL scrapers we expose. Each one runs a
// single SHOW/SELECT and is flavor-safe across the Percona 8.0/8.4/9.x versions
// we support. Scrapers gated behind kingpin CLI flags upstream (heartbeat,
// processlist, *_events_statements, ...) are intentionally excluded: they would
// drag in the exporter's flag wiring.
var scraperFiles = []string{
	"global_status.go",
	"global_variables.go",
	"slave_status.go",
	"binlog.go",
	"info_schema_innodb_cmp.go",
	"info_schema_innodb_cmpmem.go",
	"info_schema_query_response_time.go",
	"perf_schema_replication_group_member_stats.go",
	"perf_schema_replication_applier_status_by_worker.go",
}

var packageRE = regexp.MustCompile(`(?m)^package collector$`)

func main() {
	src := flag.String("src", "../../../../vendor/prometheus/mysqld_exporter/collector",
		"path to the vendored mysqld_exporter collector package")
	out := flag.String("out", ".", "destination scrapers package directory")
	flag.Parse()

	if err := run(*src, *out); err != nil {
		log.Fatalf("gen: %v", err)
	}
}

func run(src, out string) error {
	if err := purge(out); err != nil {
		return err
	}
	for _, name := range append(append([]string{}, supportFiles...), scraperFiles...) {
		if err := copyFile(filepath.Join(src, name), filepath.Join(out, name)); err != nil {
			return fmt.Errorf("%s: %w", name, err)
		}
	}
	log.Printf("gen: vendored %d files into %s", len(supportFiles)+len(scraperFiles), out)
	return nil
}

// purge removes previously generated files so deletions from the file lists do
// not leave orphans behind. Hand-written files lack genMarker and are kept.
func purge(out string) error {
	entries, err := os.ReadDir(out)
	if err != nil {
		return err
	}
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".go") {
			continue
		}
		path := filepath.Join(out, e.Name())
		data, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		if strings.HasPrefix(string(data), genMarker) {
			if err := os.Remove(path); err != nil {
				return err
			}
		}
	}
	return nil
}

func copyFile(srcPath, dstPath string) error {
	data, err := os.ReadFile(srcPath)
	if err != nil {
		return err
	}
	content := packageRE.ReplaceAllString(string(data), "package scrapers")
	if filepath.Base(srcPath) == "instance.go" {
		content, err = adaptInstance(content)
		if err != nil {
			return err
		}
	}
	return os.WriteFile(dstPath, []byte(genHeader+content), 0o644)
}

// adaptInstance rewrites the upstream constructor so the instance wraps a
// caller-supplied *sql.DB (the instance manager's metrics pool) instead of
// dialing its own connection from a DSN. The rest of the type — version/flavor
// detection, getDB/Ping/Close — is preserved so the scrapers compile unchanged.
func adaptInstance(content string) (string, error) {
	const upstream = `func newInstance(dsn string) (*instance, error) {
	i := &instance{}
	db, err := sql.Open("mysql", dsn)
	if err != nil {
		return nil, err
	}
	db.SetMaxOpenConns(1)
	db.SetMaxIdleConns(1)
	i.db = db
`
	const adapted = `// newInstance wraps an existing *sql.DB. The pool is owned by the caller; the
// returned instance never closes it. Adapted from the upstream DSN constructor
// by metrics/scrapers/gen.
func newInstance(db *sql.DB) (*instance, error) {
	i := &instance{db: db}
`
	if !strings.Contains(content, upstream) {
		return "", fmt.Errorf("instance.go: upstream newInstance signature not found; " +
			"the vendored mysqld_exporter changed, update gen/main.go")
	}
	return strings.Replace(content, upstream, adapted, 1), nil
}
